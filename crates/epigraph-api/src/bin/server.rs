use epigraph_api::metrics::Metrics;
use epigraph_api::routes::webhooks::{start_webhook_dispatcher, WebhookDeliveryConfig};
use epigraph_api::{create_router, ApiConfig, AppState};
#[cfg(feature = "db")]
use epigraph_jobs::{
    cluster_graph::ClusterGraphHandler, EpiGraphJob, JobQueue, JobRunner, PostgresJobQueue,
};
use std::sync::Arc;
#[cfg(feature = "db")]
use std::time::Duration;
use tracing_subscriber::{layer::SubscriberExt, util::SubscriberInitExt};

/// Create the embedding service based on environment configuration.
///
/// Provider priority: OpenAI (text) > Jina (multimodal fallback) > Mock (dev).
/// OpenAI must be the primary provider because claim embeddings in the database
/// are generated by `text-embedding-3-small`. Query embeddings must come from
/// the same model/vector space or cosine similarity is meaningless.
///
/// Jina is used only for multimodal (image) embedding during ingestion, not for
/// text query embedding. It is exposed via `as_multimodal()` on a separate
/// service when needed.
///
/// Environment variables:
/// - `OPENAI_API_KEY`: OpenAI API key (primary — text embeddings)
/// - `JINA_API_KEY`: Jina AI API key (multimodal figure embeddings only)
/// - `EMBEDDING_DIMENSION`: Vector dimension (default: 1536)
fn create_embedding_service() -> std::sync::Arc<dyn epigraph_embeddings::EmbeddingService> {
    let dimension: usize = std::env::var("EMBEDDING_DIMENSION")
        .ok()
        .and_then(|v| v.parse().ok())
        .unwrap_or(1536);

    // Try OpenAI first — must match the model used by backfill_embeddings.py
    if let Ok(api_key) = std::env::var("OPENAI_API_KEY") {
        if !api_key.is_empty() {
            let config = epigraph_embeddings::EmbeddingConfig::openai(dimension);
            match epigraph_embeddings::OpenAiProvider::new(config, api_key) {
                Ok(provider) => {
                    tracing::info!(dim = dimension, "Embedding service: OpenAI (text-only)");
                    return std::sync::Arc::new(provider);
                }
                Err(e) => {
                    tracing::warn!("OpenAI provider init failed: {e}, trying Jina fallback");
                }
            }
        }
    }

    // Jina fallback — works for text too, but produces different vectors than
    // what's stored in the DB. Only use if OpenAI is unavailable.
    if let Ok(api_key) = std::env::var("JINA_API_KEY") {
        if !api_key.is_empty() {
            let config = epigraph_embeddings::EmbeddingConfig::jina(dimension);
            match epigraph_embeddings::JinaProvider::new(config, api_key) {
                Ok(provider) => {
                    tracing::warn!(
                        dim = dimension,
                        "Embedding service: Jina v4 (multimodal) — \
                         text queries may not match OpenAI-embedded claims"
                    );
                    return std::sync::Arc::new(provider);
                }
                Err(e) => {
                    tracing::warn!("Jina provider init failed: {e}, falling back to mock");
                }
            }
        }
    }

    // Fallback to mock for local development
    let config = epigraph_embeddings::EmbeddingConfig::openai(dimension);
    let provider = epigraph_embeddings::MockProvider::new(config);
    tracing::info!(
        dim = dimension,
        "Embedding service: Mock (development only)"
    );
    std::sync::Arc::new(provider)
}

#[tokio::main]
async fn main() {
    dotenvy::dotenv().ok();

    let args: Vec<String> = std::env::args().collect();
    if args.iter().any(|arg| arg == "--export-openapi") {
        let spec = epigraph_api::openapi::openapi_spec();
        let json = spec
            .to_pretty_json()
            .expect("Failed to serialize OpenAPI spec");
        println!("{}", json);
        return;
    }

    // Initialize tracing subscriber for structured logging.
    //
    // When the `otel` feature is enabled and OTEL_EXPORTER_OTLP_ENDPOINT is
    // set, an additional OpenTelemetry layer is attached that exports spans via
    // OTLP (gRPC) to the configured collector endpoint.  Otherwise only the
    // human-readable stderr formatter is used.
    #[cfg(not(feature = "otel"))]
    tracing_subscriber::registry()
        .with(tracing_subscriber::EnvFilter::new(
            std::env::var("RUST_LOG").unwrap_or_else(|_| "info,epigraph_api=debug".into()),
        ))
        .with(tracing_subscriber::fmt::layer())
        .init();

    #[cfg(feature = "otel")]
    {
        // TODO(otel): When deploying with OTLP support, initialise the pipeline here:
        //
        //   let otlp_endpoint = std::env::var("OTEL_EXPORTER_OTLP_ENDPOINT").ok();
        //   if let Some(endpoint) = otlp_endpoint {
        //       let exporter = opentelemetry_otlp::new_exporter()
        //           .tonic()
        //           .with_endpoint(endpoint);
        //       let tracer = opentelemetry_otlp::new_pipeline()
        //           .tracing()
        //           .with_exporter(exporter)
        //           .with_trace_config(opentelemetry_sdk::trace::config()
        //               .with_resource(opentelemetry_sdk::Resource::new(vec![
        //                   opentelemetry::KeyValue::new("service.name", "epigraph-api"),
        //               ])))
        //           .install_batch(opentelemetry_sdk::runtime::Tokio)
        //           .expect("Failed to install OTLP tracer");
        //       let otel_layer = tracing_opentelemetry::layer().with_tracer(tracer);
        //       tracing_subscriber::registry()
        //           .with(tracing_subscriber::EnvFilter::new(
        //               std::env::var("RUST_LOG").unwrap_or_else(|_| "info,epigraph_api=debug".into()),
        //           ))
        //           .with(tracing_subscriber::fmt::layer())
        //           .with(otel_layer)
        //           .init();
        //   } else {
        //       // fall through to stderr-only
        //   }
        //
        // The exact API surface changes between opentelemetry* crate versions.
        // This block compiles (feature flag presence is what matters for CI/CD
        // deployment decisions) but the init is deferred to deployment time.
        tracing_subscriber::registry()
            .with(tracing_subscriber::EnvFilter::new(
                std::env::var("RUST_LOG").unwrap_or_else(|_| "info,epigraph_api=debug".into()),
            ))
            .with(tracing_subscriber::fmt::layer())
            .init();

        if std::env::var("OTEL_EXPORTER_OTLP_ENDPOINT").is_ok() {
            tracing::info!(
                "OTEL_EXPORTER_OTLP_ENDPOINT is set; \
                 OTLP exporter init is pending deployment configuration"
            );
        }
    }

    // Configure API settings.
    //
    // `require_signatures` enables a packet-signature gate on the write path
    // (`/api/v1/submit/packet`) that currently fails closed because the
    // Ed25519 verifier is still TODO in `routes/submit.rs`. Until that lands,
    // make it env-driven so deployments can opt out and continue recording
    // provenance via OAuth2 Bearer auth alone. Default off; flip to `1`/`true`
    // to re-enable the gate once the verifier ships.
    let require_signatures = std::env::var("EPIGRAPH_REQUIRE_SIGNATURES")
        .ok()
        .map(|v| matches!(v.to_ascii_lowercase().as_str(), "1" | "true" | "yes"))
        .unwrap_or(false);
    let config = ApiConfig {
        require_signatures,
        max_request_size: 10 * 1024 * 1024, // 10MB — figure evidence carries base64 images
    };

    // Create embedding service for semantic search
    let embedding_service = create_embedding_service();

    // Create application state — connect to PostgreSQL when db feature is enabled
    #[cfg(feature = "db")]
    let (state, job_pool) = {
        let database_url = std::env::var("DATABASE_URL")
            .expect("DATABASE_URL must be set when running with db feature");
        tracing::info!("Connecting to PostgreSQL...");
        let pool = epigraph_db::PgPool::connect(&database_url)
            .await
            .expect("Failed to connect to PostgreSQL");
        tracing::info!("PostgreSQL connected");
        let job_pool = pool.clone();
        let state = AppState::with_db(pool, config).with_embedding_service(embedding_service);
        (state, job_pool)
    };

    #[cfg(not(feature = "db"))]
    let state = AppState::new(config).with_embedding_service(embedding_service);

    // Load external identity providers from providers.toml.
    // EPIGRAPH_PROVIDERS_CONFIG overrides the default path. Missing config is
    // a hard startup error to prevent silent degradation to an empty registry.
    let state = {
        let providers_path = std::env::var("EPIGRAPH_PROVIDERS_CONFIG")
            .map(std::path::PathBuf::from)
            .unwrap_or_else(|_| std::path::PathBuf::from("providers.toml"));
        let providers = epigraph_api::oauth::providers::build_registry(providers_path.as_path())
            .expect("failed to build providers registry");
        state.with_providers(providers)
    };

    // Start background job runner with ClusterGraph handler (db feature only).
    // The runner uses PostgresJobQueue for durable persistence.  The cron loop
    // fires 60 s after startup then every 24 h thereafter.
    #[cfg(feature = "db")]
    {
        let queue: Arc<dyn JobQueue> = Arc::new(PostgresJobQueue::new(job_pool.clone()));
        let queue_for_cron = Arc::clone(&queue);

        let handler_pool = Arc::new(job_pool);
        let mut runner = JobRunner::new(2, queue);
        runner.register_handler(Arc::new(ClusterGraphHandler::new(handler_pool)));

        tokio::spawn(async move {
            runner.start().await;
        });

        tokio::spawn(async move {
            tokio::time::sleep(Duration::from_secs(60)).await;
            loop {
                match (EpiGraphJob::ClusterGraph {
                    resolution: 1.0,
                    retain_runs: 3,
                })
                .into_job()
                {
                    Ok(job) => {
                        if let Err(e) = queue_for_cron.enqueue(job).await {
                            tracing::error!(
                                error = %e,
                                "failed to enqueue nightly ClusterGraph job"
                            );
                        } else {
                            tracing::info!("Enqueued nightly ClusterGraph job");
                        }
                    }
                    Err(e) => {
                        tracing::error!(
                            error = %e,
                            "failed to serialize ClusterGraph job payload"
                        );
                    }
                }
                tokio::time::sleep(Duration::from_secs(86_400)).await;
            }
        });

        tracing::info!("Job runner started (2 workers); nightly ClusterGraph job scheduled");
    }

    // Start webhook dispatcher (subscribes to event bus for delivery)
    let _webhook_sub = start_webhook_dispatcher(
        &state.event_bus,
        state.webhook_store.clone(),
        WebhookDeliveryConfig::default(),
    );
    tracing::info!("Webhook dispatcher started");

    // Build router with all routes
    let metrics = Arc::new(Metrics::new());
    let app = create_router(state).layer(axum::Extension(metrics));

    // Bind to address. EPIGRAPH_PORT env var allows side-by-side test runs.
    let port: u16 = std::env::var("EPIGRAPH_PORT")
        .ok()
        .and_then(|s| s.parse().ok())
        .unwrap_or(8080);
    let addr = std::net::SocketAddr::from(([0, 0, 0, 0], port));

    // Check for TLS configuration via environment variables.
    // EPIGRAPH_TLS_CERT and EPIGRAPH_TLS_KEY must both be set to enable TLS.
    // In production, Caddy terminates TLS; this path is for direct-access scenarios.
    let tls_cert = std::env::var("EPIGRAPH_TLS_CERT").ok();
    let tls_key = std::env::var("EPIGRAPH_TLS_KEY").ok();

    #[cfg(feature = "tls")]
    if let (Some(cert), Some(key)) = (tls_cert, tls_key) {
        use epigraph_api::tls::TlsConfig;

        tracing::info!(
            "TLS enabled — Starting EpiGraph API server (HTTPS) on {}",
            addr
        );
        tracing::info!("Health check available at https://{}/health", addr);

        let tls_config = TlsConfig {
            cert_path: cert.into(),
            key_path: key.into(),
        };
        let rustls_config = tls_config
            .into_rustls_config()
            .await
            .expect("Failed to load TLS certificate/key");

        axum_server::bind_rustls(addr, rustls_config)
            .serve(app.into_make_service())
            .await
            .expect("TLS server error");
        return;
    }

    #[cfg(not(feature = "tls"))]
    {
        // Suppress unused variable warnings when TLS feature is disabled
        let _ = tls_cert;
        let _ = tls_key;
    }

    tracing::info!("Starting EpiGraph API server on {}", addr);
    tracing::info!("Health check available at http://{}/health", addr);

    let listener = tokio::net::TcpListener::bind(addr)
        .await
        .expect("Failed to bind to address");

    tracing::info!("Server listening on {}", addr);

    axum::serve(listener, app).await.expect("Server error");
}
